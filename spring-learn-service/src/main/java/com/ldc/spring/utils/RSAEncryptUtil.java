    /**
     * 获取私钥
     * @param keyMap 密钥对
     * @return
     * @throws Exception
     */
    public static String getPrivateKey(Map<String, Object> keyMap)
            throws Exception {
        Key key = (Key) keyMap.get(PRIVATE_KEY);
        return Base64Util.encode(key.getEncoded());
    }

    /**
     * 获取公钥
     * @param keyMap 密钥对
     * @return
     * @throws Exception
     */
    public static String getPublicKey(Map<String, Object> keyMap)
            throws Exception {
        Key key = (Key) keyMap.get(PUBLIC_KEY);
        return Base64Util.encode(key.getEncoded());
    }

    public static String getPublicKey(String rsaKeyRelativePath) throws Exception {
        return readKey(ClassPathUtil.getRelativePathForTest() + "/config/cer/common_public.cer");
    }

    public static String getPrivateKey(String rsaKeyRelativePath) throws Exception {
        return readKey(ClassPathUtil.getRelativePathForTest() + "/config/cer/common_private.cer");
    }

    public static String readKey(String filePath) throws Exception {
        File keyFile = new File(filePath);
        return fileRead(keyFile);
    }

    private static String fileRead(File file) throws Exception {
        FileReader reader = null;
        BufferedReader bReader = null;
        StringBuilder sb = null;
        try{
            reader = new FileReader(file);//定义一个fileReader对象，用来初始化BufferedReader
            bReader = new BufferedReader(reader);//new一个BufferedReader对象，将文件内容读取到缓存
            sb = new StringBuilder();//定义一个字符串缓存，将字符串存放缓存中
            String s = "";
            while ((s =bReader.readLine()) != null) {//逐行读取文件内容，不读取换行符和末尾的空格
                sb.append(s);//将读取的字符串添加换行符后累加存放在缓存中
            }
        }finally {
            if(bReader != null)
                bReader.close();
            if(reader != null)
                reader.close();
        }
        return sb.toString();
    }
